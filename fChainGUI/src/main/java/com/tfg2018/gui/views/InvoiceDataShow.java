    public InvoiceDataShow(String userName, KeyPair userKeyPair, Factura invoice) {
        initComponents();
        Participant emisor = invoice.getEmisor();
        Participant receptor = invoice.getReceptor();
        this.invoice = invoice;
        this.userName = userName;
        this.userKeyPair = userKeyPair;
        userNameLabel.setText(userName);
        String invoiceInfo = "*****************************EMISOR*****************************\n";
        invoiceInfo += invoiceParticipant(invoice, emisor);
        invoiceInfo += "\n****************************RECEPTOR****************************\n";
        invoiceInfo += invoiceParticipant(invoice, receptor);
        invoiceInfo += "\n****************************CONCEPTO****************************\n";
        invoiceInfo += invoiceConcept(invoice);
        invoiceInfo += "\n*****************************TASAS******************************\n";
        invoiceInfo += invoiceTasas(invoice);
        invoiceInfo += "\n*****************************INFO*******************************\n";
        invoiceInfo += invoiceInfo(invoice, invoice.getComprobante());
        this.invoiceInfoTextArea.setText(invoiceInfo);
    }

    public String invoiceTasas(Factura invoice) {
        String result = "";
        String aux[] = invoice.getImpuesto();
        for (int i = 0; i < aux.length; i++) {
            result += "impuesto " + (i + 1) + " --> " + aux[i] + '\n';
        }
        return result;
    }

    public String invoiceConcept(Factura invoice) {
        String result = "";
        String aux[] = invoice.getConcept();
        for (int i = 0; i < aux.length; i++) {
            result += "concepto " + (i + 1) + " --> " + aux[i] + '\n';
        }
        return result;
    }

    public String invoiceParticipant(Factura invoice, Participant participant) {
        String result = "";
        result += "Nombre --> " + participant.getNombre() + '\n';
        result += "Rfc --> " + participant.getRfc() + '\n';
        result += "País --> " + participant.getLocalizacion().getPaisEmisor() + '\n';
        result += "Estado --> " + participant.getLocalizacion().getEstadoEmisor() + '\n';
        result += "Municipio --> " + participant.getLocalizacion().getMunicipioEmisor() + '\n';
        result += "Código Postal --> " + participant.getLocalizacion().getCPEmisor() + '\n';
        result += "Calle --> " + participant.getLocalizacion().getCalleEmisor() + '\n';
        return result;
    }

    public String invoiceInfo(Factura invoice, ComprobanteInfo comprobante) {
        String result = "";
        result += "Fecha de expedición --> " + comprobante.getFecha() + '\n';
        result += "Forma de pago --> " + comprobante.getFormaDePago() + '\n';
        result += "Lugar de expedición --> " + comprobante.getLugarExpedicion() + '\n';
        result += "Método de pago --> " + comprobante.getMetodoDePago() + '\n';
        result += "Subtotal --> " + comprobante.getSubTotal() + '\n';
        result += "Total --> " + comprobante.getTotal() + '\n';
        return result;
    }

